
#include "main.h"
/**
 * main - Entry point
 * Descreption: A program that prints the alphabet in lowercase
 * Return: always 0 (Success)
 */


int main(void)
{
	void print_alphabet(void);
	{
		int EA;

		for (EA = 'a'; EA <= 'z'; EA++)
		{
			_putchar(EA);
			_putchar('\n');
		}
	}

	return (0);


}
